def print_even_numbers_in_range():
    """
    программа запрашивает у пользователя
    два числа и выводит все четные числа в этом диапазоне
    """
    try:
        # Запрашиваем у пользователя начало и конец диапазона
        start = int(input("Введите начало диапазона: "))
        end = int(input("Введите конец диапазона: "))

        print(f"Четные числа в диапазоне от {start} до {end}:")

        # Перебираем все числа в заданном диапазоне
        for number in range(start, end + 1):
            # Проверяем, является ли число четным
            if number % 2 == 0:
                print(number)

    except ValueError:
        print("Ошибка: введите корректные числовые значения.")

# Вызов функции
print_even_numbers_in_range()