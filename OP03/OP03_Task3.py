def get_days_in_month():
    """
    - Возвращает количество дней в месяце без учета високосного года.
    - Используется словарь days_in_month для сопоставления номера месяца с количеством дней.
    - Проверяется корректность ввода номера месяца.
    - Обрабатываются исключения, если пользователь вводит нечисловое значение.
    """
    # Словарь, сопоставляющий номер месяца с количеством дней
    days_in_month = {
        1: 31, 2: 28, 3: 31, 4: 30, 5: 31, 6: 30, 7: 31, 8: 31, 9: 30, 10: 31, 11: 30, 12: 31
    }

    while True:
        try:
            # Запрашиваем у пользователя номер месяца
            month = int(input("Введите номер месяца (1-12): "))
            if month < 1 or month > 12:
                print("Некорректный номер месяца. Введите число от 1 до 12.")
            else:
                # Возвращаем количество дней в месяце
                days = days_in_month[month]
                print(f"Количество дней в месяце: {days}")
                break
        except ValueError:
            print("Ошибка: введите числовое значение.")

# Вызов функции без аргументов
get_days_in_month()
