# Пошаговый алгоритм действий :
# 1. Получить строку для проверки.
# 2. Привести строку к одному регистру (например, к нижнему), чтобы сравнение было нечувствительно к регистру.
# 3. (Опционально) Удалить из строки все небуквенно-цифровые символы (например, пробелы, знаки препинания).
# 4. Сравнить строку с её зеркальным отображением (реверсом).
# 5. Если строка совпадает с реверсом(строка в обратном порядке) — это палиндром, иначе — нет.

def is_palindrome(s):
    # Приводим строку к нижнему регистру
    s = s.lower()
    # Удаляем все небуквенно-цифровые символы
    s = ''.join(char for char in s if char.isalnum())
    # Сравниваем строку с её обратной версией
    return s == s[::-1]

print(is_palindrome("А роза упала на лапу Азора"))  # True
print(is_palindrome("Hello, World!"))               # False