import pandas as pd
import numpy as np

"""
код создаёт DataFrame с оценками 10 учеников по 5 предметам, 
выводит первые несколько строк, вычисляет и выводит средние и медианные оценки, 
вычисляет первый и третий квартили для оценок по математике, 
а также стандартное отклонение для каждого предмета.
"""


# Шаг 1: Создать DataFrame с данными
data = {
	'Имя': ['Алексей', 'Мария', 'Иван', 'Ольга', 'Павел', 'Светлана', 'Дмитрий', 'Елена', 'Анна', 'Николай'],
	'Математика': [78, 85, 92, 68, 74, 88, 90, 76, 85, 82],
	'Физика': [83, 79, 95, 72, 78, 91, 89, 80, 84, 87],
	'Химия': [85, 82, 88, 75, 80, 85, 92, 81, 86, 84],
	'Литература': [90, 88, 93, 82, 78, 85, 94, 89, 87, 86],
	'История': [88, 84, 90, 80, 82, 86, 91, 85, 88, 87]
}

df = pd.DataFrame(data)

# Шаг 2: Выведите первые несколько строк DataFrame
print("Первые несколько строк DataFrame:")
print(df.head())

# Шаг 3: Вычислите среднюю оценку по каждому предмету
average_scores = df.mean(numeric_only=True)
print("\nСредние оценки по каждому предмету:")
print(average_scores)

# Шаг 4: Вычислите медианную оценку по каждому предмету
median_scores = df.median(numeric_only=True)
print("\nМедианные оценки по каждому предмету:")
print(median_scores)

# Шаг 5: Вычислите Q1 и Q3 для оценок по математике
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
IQR_math = Q3_math - Q1_math
print("\nQ1 для математики:", Q1_math)
print("Q3 для математики:", Q3_math)
print("IQR для математики:", IQR_math)

# Шаг 6: Вычислите стандартное отклонение
std_deviation = df.std(numeric_only=True)
print("\nСтандартное отклонение по каждому предмету:")
print(std_deviation)
