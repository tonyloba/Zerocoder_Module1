def run_kibosh() -> None:
  # Программа "Чепуха" на Python

  # Запросим у пользователя слова
  print("Введите пять разных слов для создания чепухи.")
  word1 = input("Введите существительное: ")
  word2 = input("Введите прилагательное: ")
  word3 = input("Введите глагол в прошедшем времени: ")
  word4 = input("Введите местоимение: ")
  word5 = input("Введите наречие: ")

  # Заготовленный текст с местами для вставки слов
  template = f"""
  Однажды {word1} {word2} {word3} на большую гору.
  {word4} было очень важно, чтобы все произошло именно так.
  Ведь если бы не {word1}, то {word4} никогда бы не {word3} так {word5}.
  """

  # Выводим чепуху на экран
  print("Ваша чепуха:")
  print(template)

if __name__ == "__main__":
  run_kibosh()
